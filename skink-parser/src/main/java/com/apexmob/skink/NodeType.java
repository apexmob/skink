package com.apexmob.skink;

/**
 * The NodeType enumeration describes the different types of document tree nodes that can be processed.
 */
public enum NodeType {
	/**
	 * Start element.
	 */
	START_ELEMENT,
	
	/**
	 * Text node.
	 */
	TEXT,
	
	/**
	 * End Element.
	 */
	END_ELEMENT
}
